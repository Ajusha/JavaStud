
public class JavaDataTypes {
	public static void main(String[] args) {
		
		//Primitive datatypes
		boolean b = true;
		
		char c = 'X';
		char ch = 88;
		char n = 'न';
		
		byte b2 = 127;
		short x = 1000;
		int i = 687687;
		long l = 869868698L;
		
		float f = 98.6f;
		double d = 243234.34;
		
		// Non Primitive: String, Array
		String s = "HELLO नेपाल";
		System.out.println(s);
		
		int[] arr = new int[5];
		
	}
}
